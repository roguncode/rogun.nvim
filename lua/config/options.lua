vim.opt.number = true --displays line numbers
vim.opt.cursorline = true --displays a line when a cursor is placed somewhere 
vim.opt.relativenumber = true -- line numbers are displayed as relative to where you are
vim.opt.expandtab = true -- inserts white spaces instead of tabs 
vim.opt.shiftwidth = 2 --how many spaces to use for one level of indendation
vim.opt.tabstop = 2 --how wide a tab character should be, in this case 2 spaces wide
vim.opt.smartindent = true -- automatically add new indentation when a new line starts
vim.opt.showmode = false -- disables the traditional insert mode, visual mode present in vim
